if [ "$1" = "-u" ]; then
	unique=1
	shift
fi
if [ "$1" != "" ]; then
	num=$1
else
	num=1
fi
result=""
while [ $num -gt 0 ]
do
	id=$((RANDOM % count + 1))
	word=`echo $words | cut -f $id -d ' '`

	echo "$result" | grep "\<$word\>" > /dev/null
	if [ $? -ne 0 ] || [ $unique -eq 0 ]; then
		result="$result $word"
		num=$((num - 1))
	fi
done
echo $result

